public class Quadratgleichung {
    public static void main ( String[] args ) {

        double x = 4.0;
        x = ((3 * x * x) - (8 * x)) + 4;
        System.out.println(x);
        x = 2.0;
        x = ((3 * x * x) - (8 * x)) + 4;
        System.out.println(x);
        x = 3.0;
        x = ((3 * x * x) - (8 * x)) + 4;
        System.out.println(x);
    }
}
